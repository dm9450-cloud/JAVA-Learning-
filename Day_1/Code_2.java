// He has to print the number of triplets chosen from the array that can make triangles if we take them as side lengths of a triangle.

// Note:- Input has been handled in the driver code. You need to complete ValidTriangle() Class.

// Input Format
// First line contains an integer n.

// Second line contains an integer array nums of size n.

// Output Format
// print the number of triplets chosen from the array.

// Example 1
// Input

// 4 
// 2 2 3 4
// Output

// 3

// Explanation

// Valid combinations are: 
// * 2,3,4 (using the first 2) 
// * 2,3,4 (using the second 2)
// * 2,2,3

// Example 2
// Input

// 3 
// 3 4 5

// Output

// 1

// Explanation

// Only one valid combination : {3 , 4 , 5}

