package Day_4;

public class Code_12 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a==b);   //false
        System.out.println(a!=b);  //True
        System.out.println(a>b);   //False
        System.out.println(a<b);   //True;
        System.out.println(a>=b); //False;
        System.out.println(a<=b); //True;
    }
}
