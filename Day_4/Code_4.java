package Day_4;

public class Code_4 {
    public static void main(String[] args) {
        System.out.println(15 + 30); // 45  //Addition Operator-- integer addition
        System.out.println(15.0 + 30); //45.0 //Addition operator - Double Addition 
        System.out.print("15 + 30"); // 15 + 30 //character 
        System.out.println("15" + "30");  //1538 ///Concatination

        System.out.println("15" + " " + "30"); //15 30 //Concatination

        System.out.println("a" + "B"); //aB //Concatination

        System.out.println(15 + "B");   //15B //Concatination

        System.out.println("15" + "30"); //1530 //Character
        System.out.println("15" + 30); //1530  //concatination
        System.out.println(15 + "60"); //1560   //concatination
        System.out.println(15 + 30 + "15 + 30" + 15 + 30); //4515 + 301530 //concatination


    }
}
