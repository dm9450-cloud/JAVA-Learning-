
package Day_4;

public class Code_1 {
   public static void main(String[] args) {
        int a = 10;
      
        System.out.println(a++);   //10
        System.out.println(a);   //11

        System.out.println(++a);   //12
        System.out.println(a);  //12

        int b = 10;
        System.out.println(b--);  //10
        System.out.println(b);   //9

        System.out.println(--b);   //8
        System.out.println(b);    //8
   } 
}
